let numberA = 9;
let numberB = 3;
let operation = "*";

if (operation == "+") console.log(numberA + numberB);
else if (operation == "-") console.log(numberA - numberB);
else if (operation == "*") console.log(numberA * numberB);
else if (operation == "/") console.log(numberA / numberB);
else console.log("error");
